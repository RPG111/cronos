export async function seedWorldTeams(): Promise<number> {
  const teamsCol = collection(db, "teams");
  const BATCH_SIZE = 499;
  let count = 0;

  for (let i = 0; i < TEAM_NAMES.length; i += BATCH_SIZE) {
    const batch = writeBatch(db);
    const chunk = TEAM_NAMES.slice(i, i + BATCH_SIZE);
    for (const name of chunk) {
      const slug = slugify(name);
      const ref = doc(teamsCol, slug);
      batch.set(
        ref,
        { name, slug, source: "seed", createdAt: serverTimestamp() },
        { merge: true }
      );
      count++;
    }
    await batch.commit();
  }

  return count;
}
